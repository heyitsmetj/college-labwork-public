import java.io.*;

class Arithmatic
{
	public static void main(String args[])
	{
		int a = 10,b = 2;
		int sum = a + b;
		int sub = a - b;
		int div = a / b;
		int mul = a * b;
		
		System.out.println(a + " + " + b + " = " + sum);
		System.out.println(a + " - " + b + " = " + sub);
		System.out.println(a + " * " + b + " = " + mul);
		System.out.println(a + " / " + b + " = " + div);
	
	}
}
